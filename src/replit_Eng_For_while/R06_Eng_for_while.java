package replit_Eng_For_while;

public class R06_Eng_for_while {
    public static void main(String[] args) {
        /*
        Print Alphabet on the console.
        OUTPUT : a b c .. .. .. .. y z
         */

        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");
        }
    }
}
