package replit_Eng_For_while;

public class R08_Eng_for_while {
    /*
    Ascii value of a = 97 Ascii value of b = 98 Ascii value of c = 99
     */
    public static void main(String[] args) {


        for (char i = 'a'; i <= 'z'; i++) {
            int ascii = (int) i;
            System.out.println("Ascii value of " + i + " = " + ascii);

        }

    }
}
